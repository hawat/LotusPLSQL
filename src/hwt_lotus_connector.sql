<pre class="brush: sql">create or replace procedure hwt_send_to_graphite(key in varchar2, value in number) as
  bt_conn    utl_tcp.connection;
  retval     binary_integer;
  l_sequence varchar2(255);
  timestamp  number;
begin
  bt_conn := utl_tcp.open_connection(remote_host =&gt; 'graphite.mynet.net', -- log server
                                      remote_port =&gt; 2003, -- port
                                     tx_timeout  =&gt; 1);
  
  SELECT (sysdate - to_date('01-STY-1970', 'DD-MON-YYYY')) * (86400) -
         (2 * 60 * 60)
    into timestamp
    FROM dual;
  l_sequence := key ||' ' || value || ' ' || trunc(timestamp) || CHR(10);
  dbms_output.put_line(l_sequence);
  retval := utl_tcp.write_line(bt_conn, l_sequence);
  utl_tcp.flush(bt_conn);
